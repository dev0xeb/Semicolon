#collect number in pounds from user
# multiply the number in pounds from the user by the 0.454 which therefore gives the value of whatever the user entered in kilograms

value_in_pounds = int(input('Enter the weight in pounds you want to convert to kilograms: '))

value_in_kilograms = value_in_pounds * 0.454

print(f'The value of {value_in_pounds} in kilograms is {value_in_kilograms}')

